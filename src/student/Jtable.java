     private void Facultytable()
     {
          String query = "select * from Faculty";
         Connection c = null;
       
        
     
        PreparedStatement ps = null;
        try {
            Class.forName("org.sqlite.JDBC");
      c = DriverManager.getConnection("jdbc:sqlite:std.db");
     
      
     
            ps = c.prepareStatement(query);
            ResultSet rs = ps.executeQuery();
            DefaultTableModel model = (DefaultTableModel) Table.getModel();
        int i =0;
        while (rs.next()) {
           String F_id = rs.getString("F_id");
           String F_name = rs.getString("Name");
           String F_contact = rs.getString("Contact");
           String C_Code = rs.getString("C_Code"); 
            model.addRow(new Object[]{F_id, F_name,F_contact, C_Code});
            i++; 
        }
        if(i <1)
        {
            JOptionPane.showMessageDialog(null, "No Record Found","Error",
            JOptionPane.ERROR_MESSAGE);
        }
    }
          catch (SQLException ex) {
            System.out.println(ex.getMessage());
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Jtable.class.getName()).log(Level.SEVERE, null, ex);
        }
     }

