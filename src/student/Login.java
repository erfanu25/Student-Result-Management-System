    private void bActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bActionPerformed
    if(evt.getSource()==b)
    {
       id=(t1.getText().trim()); 
       pass=(t2.getText().trim());
       l=Levelcombo.getSelectedItem().toString(); 
        switch (l) {              //switching to the user
            case "Admin":
                level="1";
                break;
            case "Faculty":
                level="2";
                break;
            case "Student":
                level="3";
                break;
            default:
                break;
        }
        
         boolean status=false; 
          Connection c = null;
           Statement stmt = null;
    try {
      Class.forName("org.sqlite.JDBC");
      c = DriverManager.getConnection("jdbc:sqlite:std.db");
      stmt = c.createStatement();
     
      String sql = "SELECT * FROM `Login` WHERE ID=? and Pass=? and Level=?;";   //sql for login
     //System.out.println(id+pass+level);
      
      PreparedStatement ps=c.prepareStatement(sql);  
            
      ps.setString(1,id);
      ps.setString(2,pass);
      ps.setString(3,level);
         
      
ResultSet rs=ps.executeQuery();  
status=rs.next();
      stmt.close();
      c.close();
      if(status==true && level.equals("1"))       //passing user to the destination
      {  
          dispose();
         new Adminpanel();
          //System.out.println("Admin login complete");
      }
      
      else if(status==true && level.equals("2"))
      {
          dispose();
          new F_pinfo();
          //System.out.println("Faculty login complete");
      }
      
      else if(status==true && level.equals("3"))
      {
          dispose();
          new S_pinfo();
          //System.out.println("Student login complete");
      }
      
      else
      {   
          JOptionPane.showMessageDialog(null, "Wrong ID and Password","MESSAGE",
            JOptionPane.ERROR_MESSAGE);
      }
    } catch ( Exception e ) {
      System.err.println( e.getClass().getName() + ": " + e.getMessage() );
      System.exit(0);
    }
    }        
    }//GEN-LAST:event_bActionPerformed

