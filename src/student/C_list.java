    private void CourseTable(){
        String query = "select * from Course";
       Connection c = null;
    
 
        PreparedStatement ps;
        try {
            Class.forName("org.sqlite.JDBC");
      c = DriverManager.getConnection("jdbc:sqlite:std.db");
     
            ps = c.prepareStatement(query);
            ResultSet rs = ps.executeQuery();
            DefaultTableModel model = (DefaultTableModel) CTable.getModel();
        int i =0;
        while (rs.next()) {
           String emp_id = rs.getString("C_Code");
           String emp_name = rs.getString("C_Name");
           String emp_addres = rs.getString("C_Credit");
        
            model.addRow(new Object[]{emp_id, emp_name, emp_addres});
            i++; 
        }
        if(i <1)
        {
            JOptionPane.showMessageDialog(null, "No Record Found","Error",
            JOptionPane.ERROR_MESSAGE);
        }
    }
          catch (SQLException | ClassNotFoundException ex) {
            Logger.getLogger(Jtable.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

