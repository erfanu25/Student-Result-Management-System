    public final void Result(){
         String query = "select * from Course";   //getting all courses From database
       Connection c = null;
    
 
        PreparedStatement ps,ps1;
        try {
            Class.forName("org.sqlite.JDBC");
      c = DriverManager.getConnection("jdbc:sqlite:std.db");
     
            ps = c.prepareStatement(query);
            ResultSet rs = ps.executeQuery();
            DefaultTableModel model = (DefaultTableModel) RTable.getModel();
        int i =0;
        while (rs.next()) {
            grade="";
           String C_Code = rs.getString("C_Code");
           String C_name = rs.getString("C_Name");
           String C_hr = rs.getString("C_Credit");
           
        String strQuery = "select * from $temp where S_id=(?)";   //Checking student take a course or not
        String qry =strQuery.replace("$temp",C_Code);
        ps1 = c.prepareStatement(qry);
        ps1.setString(1,id);
        ResultSet rs1 = ps1.executeQuery();
        while (rs1.next()) {
            grade = rs1.getString("Grade");                      // takin grade from database 
            //System.out.println(rs1.getString("Grade"));
        }
        if(!grade.equals("")){
            
                switch (grade) {
                    case "A":
                        point=4;
                        break;
                    case "B":
                        point=3;
                        break;
                    case "c":
                        point=2;
                        break;
                    case "D":
                        point=1;
                        break;
                    default:
                        point=0;
                        break;
                }
                chr=Float.valueOf(C_hr);
                totalcredit+=chr;     //determinig total credit of a student
                CG+=(chr *point);   //CGPA calculation
                     
        model.addRow(new Object[]{C_Code, C_name, C_hr,grade});     // publishing result in result table        
        }
        i++;
        }
        CG=CG/totalcredit;
        if(i <1)
        {
            JOptionPane.showMessageDialog(null, "No Record Found","Error",
            JOptionPane.ERROR_MESSAGE);
        }
    }
          catch (SQLException | ClassNotFoundException ex) {
            Logger.getLogger(Jtable.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
