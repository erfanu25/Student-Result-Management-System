    private void InsertbuttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_InsertbuttonActionPerformed

        sid =(T1.getText().trim());

        Connection c = null;

        try {
            Class.forName("org.sqlite.JDBC");
            c = DriverManager.getConnection("jdbc:sqlite:std.db");

            //Inserting Student to the course

            String sql = "INSERT INTO $temp (S_id,Grade) " +
            "VALUES (?,?);";

            String query =sql.replace("$temp",C_Cod);
            PreparedStatement ps = c.prepareStatement(query);
            ps.setString(1,sid);
            ps.setString(2,"NA");
            ps.executeUpdate();

            DefaultTableModel model = (DefaultTableModel) Table.getModel();
            model.addRow(new Object[]{sid});

            JOptionPane.showMessageDialog(null, "Record inserted successfully.","MESSAGE",
                JOptionPane.PLAIN_MESSAGE);

            c.close();

        }

        catch ( ClassNotFoundException | SQLException e ) {
            // System.err.println( e.getClass().getName() + ": " + e.getMessage() );
            JOptionPane.showMessageDialog(null, "Already Inserted","MESSAGE",
                JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_InsertbuttonActionPerformed

    private void DeletebuttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeletebuttonActionPerformed
        sid =(T1.getText().trim());

        Connection c = null;

        try {
            Class.forName("org.sqlite.JDBC");
            c = DriverManager.getConnection("jdbc:sqlite:std.db");

            String sql = "DELETE FROM $temp WHERE  S_id=(?); " ;

            String query =sql.replace("$temp",C_Cod);
            //System.out.println(F_id+Name+Contact+C_Code);
            PreparedStatement ps = c.prepareStatement(query);
            ps.setString(1,sid);
            ps.executeUpdate();

            DefaultTableModel model = (DefaultTableModel) Table.getModel();
            model.removeRow(Table.getSelectedRow());

            JOptionPane.showMessageDialog(null, "Record deleted successfully.","MESSAGE",
                JOptionPane.ERROR_MESSAGE);

            c.close();

        }

        catch ( ClassNotFoundException | SQLException e ) {
            JOptionPane.showMessageDialog(null, "Something Wrong","MESSAGE",
                JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_DeletebuttonActionPerformed

